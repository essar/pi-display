package it.essar.pidisplay.display;

public class ControlChannelException extends RuntimeException {

	public ControlChannelException() {
		
		super();

	}

	public ControlChannelException(String message) {

		super(message);
		
	}

	public ControlChannelException(Throwable cause) {
		
		super(cause);
		
	}

	public ControlChannelException(String message, Throwable cause) {
		
		super(message, cause);
		
	}

	public ControlChannelException(String message, Throwable cause, boolean arg2, boolean arg3) {
		
		super(message, cause, arg2, arg3);

	}
}
