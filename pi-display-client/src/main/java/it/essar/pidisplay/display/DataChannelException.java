package it.essar.pidisplay.display;

public class DataChannelException extends RuntimeException {

	public DataChannelException() {
		
		super();

	}

	public DataChannelException(String message) {

		super(message);
		
	}

	public DataChannelException(Throwable cause) {
		
		super(cause);
		
	}

	public DataChannelException(String message, Throwable cause) {
		
		super(message, cause);
		
	}

	public DataChannelException(String message, Throwable cause, boolean arg2, boolean arg3) {
		
		super(message, cause, arg2, arg3);

	}
}
